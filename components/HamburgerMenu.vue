  name: 'HamburgerMenu',
  data() {
    return {
      isMenuOpen: false, // メニューの表示状態を管理するフラグ
    };
  },
  methods: {
    hambergerMenuOnClick() {
      this.isMenuOpen = !this.isMenuOpen; // メニューの表示状態を切り替える

      // メニューが開かれている場合
